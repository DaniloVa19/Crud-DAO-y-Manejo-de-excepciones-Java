    public void rellenarTabla() {
        FuncionariosController funcionariosController = new FuncionariosController();
        try {
            List<Funcionarios> funcionarios = funcionariosController.getAllFuncionarios();
            if (funcionarios.isEmpty()) {
                System.out.println("No hay datos");
            } else {
                model.setRowCount(0);

                funcionarios.forEach(persona -> {

                    model.insertRow(TableFuncionario.getRowCount(), new Object[]{
                        persona.getNombres(),
                        persona.getApellidos(),
                        persona.getTipoIdentificacion(),
                        persona.getApellidos(),
                        persona.getEstadoCivil(),
                        persona.getNumeroIdentificacion(),
                        persona.getTelefono(),
                        persona.getSexo(),
                        persona.getFechaNacimiento(),
                        persona.getIdentificacion()
                    });

                });
            }
            //System.out.println((String) sexoCrear.getSelectedItem());

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
