    public void guardarFuncionarioDesdeFormulario() {

        try {
            Funcionarios funcionario = new Funcionarios();
            FuncionariosController funcionariosController = new FuncionariosController();
            
            PersonaController personaController = new PersonaController();

            funcionario.setNombres(nombreCrear.getText());
            funcionario.setApellidos(apellidosCrear.getText());
            funcionario.setDireccion(direccionCrear.getText());
            funcionario.setEstadoCivil(estadoCivilCrear.getText());
            funcionario.setTipoIdentificacion(tipoIdentificacionCrear.getText());
            

            funcionario.setSexo((String) sexoCrear.getSelectedItem());
            funcionario.setFechaNacimiento(LocalDate.parse(fechaDeNacimientoCrear.getText()));
            
            funcionario.setNumeroIdentificacion(numeroIdentificacionCrear.getText());
            funcionario.setIdentificacion(Integer.parseInt(idFuncionarioCrear.getText()));

            personaController.createPersona(funcionario);
            
            funcionario.setIdPersona(personaController.obtenerUltimoIdPersona());


            funcionariosController.createFuncionario(funcionario);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

